typedef unsigned long long int datatype;

datatype dp[65][65];
datatype trib(int n, int back)
{
    if(n<=1) return 1;
    if(dp[n][back]!=0)return dp[n][back];
    dp[n][back]=1;
    for(int i=1;i<=back;i++)
         dp[n][back]+=trib(n-i,back);
    return dp[n][back];
}
